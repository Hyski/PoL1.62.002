#include "precomp.h"
#include "IWorld.h"

static IWorld *g_world = 0;

//=====================================================================================//
//                                   World::World()                                    //
//=====================================================================================//
IWorld::IWorld()
{
	assert( g_world == 0 );
	g_world = this;
}

//=====================================================================================//
//                                  IWorld::~IWorld()                                  //
//=====================================================================================//
IWorld::~IWorld()
{
	assert( g_world == this );
	g_world = 0;
}

//=====================================================================================//
//                                IWorld *IWorld::Get()                                //
//=====================================================================================//
IWorld *IWorld::Get()
{
	return g_world;
}
