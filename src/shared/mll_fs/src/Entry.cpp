#include "precomp.h"
#include <mll/fs/entry.h>
#include "CriticalSection.h"

namespace mll
{

namespace fs
{

//=====================================================================================//
//                                   entry::entry()                                    //
//=====================================================================================//
entry::entry(critical_section *sect)
:	m_csect(sect)
{
	m_csect->enter();
}

//=====================================================================================//
//                                   entry::~entry()                                   //
//=====================================================================================//
entry::~entry()
{
	m_csect->leave();
}

}

}