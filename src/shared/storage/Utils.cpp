#include "precomp.h"

namespace Storage
{

//=====================================================================================//
//                           Version Utils::restoreVersion()                           //
//=====================================================================================//
Version Utils::restoreVersion(mll::io::ibinstream &in)
{
	Version v;
	in >> v;
	return v;
}

//=====================================================================================//
//                           std::string Utils::restoreTag()                           //
//=====================================================================================//
std::string Utils::restoreTag(mll::io::ibinstream &in)
{
	std::string s;
	in >> s;
	return s;
}

//=====================================================================================//
//                               void Utils::storeTag()                                //
//=====================================================================================//
void Utils::storeTag(mll::io::obinstream &out, const std::string &t)
{
	out << t;
}

//=====================================================================================//
//                             void Utils::storeVersion()                              //
//=====================================================================================//
void Utils::storeVersion(mll::io::obinstream &out, const Version &v)
{
	out << v;
}

}