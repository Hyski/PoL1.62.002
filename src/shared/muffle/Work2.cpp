#include "precomp.h"
#include "Work2.h"

namespace Muffle2
{

static volatile long g_instanceCount = 0;

//=====================================================================================//
//                                    Work::Work()                                     //
//=====================================================================================//
Work::Work()
{
	InterlockedIncrement(&g_instanceCount);
}

//=====================================================================================//
//                                    Work::~Work()                                    //
//=====================================================================================//
Work::~Work()
{
	InterlockedDecrement(&g_instanceCount);
}

//=====================================================================================//
//                            long Work::getInstanceCount()                            //
//=====================================================================================//
long Work::getInstanceCount()
{
	return g_instanceCount;
}

}
